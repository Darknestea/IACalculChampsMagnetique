def set_run_session(extension, filename=SAVE_PATH[0] + "run_counter.dat"):
    global EXTENSION
    EXTENSION = extension
    with open(filename, "a+") as f:
        f.seek(0)
        global RUN_SESSION
        RUN_SESSION = int(f.read() or 0) + 1
        f.seek(0)
        f.truncate()
        f.write(str(RUN_SESSION))

